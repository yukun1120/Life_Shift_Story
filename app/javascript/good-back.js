function good() {
  const articles = document.querySelectorAll(".good-main");
  articles.forEach(function(goods) {
    goods.addEventListener('mouseover', function() {
      this.setAttribute("style", "box-shadow: 0 10px 25px 0 rgba(0, 0, 0, 0.5);");
    });
    goods.addEventListener('mouseout', function() {
      this.removeAttribute("style", "box-shadow: 0 10px 25px 0 rgba(0, 0, 0, 0.5);");
    });
  });
}

window.addEventListener("load", good);